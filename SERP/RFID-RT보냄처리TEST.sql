/* 기타 */

SELECT *
  FROM SMS.VIEW_COMM_CODE;


SELECT A.COMM_CD
     , /*그룹 코드*/
    A.COMM_DTL_CD
     , /*상세 코드*/
    B.COMM_DTL_NM /*상세 코드 명*/
  FROM ADM_DTL_CD A
           LEFT OUTER JOIN
       ADM_COMM_DTL_CD_NM B
       ON A.COMM_CD = B.COMM_CD
           AND A.COMM_DTL_CD = B.COMM_DTL_CD
           AND B.LANG_CD = 'ko_KR'
 WHERE A.COMM_CD IN ('RT_RSN', 'DIST_TYP_CD')
   AND A.USE_YN = 'Y'
   AND B.USE_YN = 'Y'
 ORDER BY A.COMM_CD, A.SORT;


