-- 권한 조회 및 부여
SELECT * FROM DBA_TAB_PRIVS WHERE GRANTEE = 'EAI' AND TABLE_NAME = 'IF_GERP_AP_SND'; -- READUSER 스키마
GRANT SELECT, UPDATE, INSERT ON SERP_IF.IF_GERP_EXP_SHIP_RCV TO EAI; -- 부여할 스키마

--  사업자 번호 찾기
SELECT BRAND_CD, SHOP_CD, SHOP_NM, SHOP_ABBR_NM, BRN, CUS_DE
  FROM BIM_SHOP
 WHERE 1 = 1
   AND REGEXP_REPLACE( BRN, '[^0-9]' ) IN
       ('1380240510', '3141634701', '4168527929', '6881001225', '6091872136', '2063222660')
ORDER BY BRAND_CD, SHOP_CD;